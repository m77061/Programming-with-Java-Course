public class DataTypes {
    public static void main(String[] args) {
        byte byteVariable = -1;
        int intVariable = 34893434;
        float floatVariable = -3.14F;
        short firstShortVariable = -129;
        short secondShortVariable = 250;
        long longVariable = 94938483434L;
        boolean booleanVariable = false;
        char charVariable = '$';
        String stringVariable = "@";
        String anotherStringVariable = "java";
        char anotherCharVariable = 'J';

        System.out.println(byteVariable);
        System.out.println(intVariable);
        System.out.println(floatVariable);
        System.out.println(firstShortVariable);
        System.out.println(secondShortVariable);
        System.out.println(longVariable);
        System.out.println(booleanVariable);
        System.out.println(charVariable);
        System.out.println(stringVariable);
        System.out.println(anotherStringVariable);
        System.out.println(anotherCharVariable);
    }
}